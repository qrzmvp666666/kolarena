-- NOWPayments payment flow
create extension if not exists pgcrypto;

create table if not exists public.payment_orders (
  id uuid primary key default gen_random_uuid(),
  user_id uuid not null,
  plan_id bigint not null references public.membership_plans(id),
  plan_name text not null,
  plan_family text not null,
  plan_duration text not null,
  price_amount numeric not null,
  price_currency text not null,
  pay_currency text not null,
  pay_amount numeric,
  pay_address text,
  invoice_url text,
  tx_hash text,
  provider text not null default 'nowpayments',
  provider_order_id text,
  provider_payment_id text,
  provider_status text,
  status text not null default 'pending',
  raw_create_response jsonb,
  raw_last_webhook jsonb,
  membership_applied boolean not null default false,
  membership_apply_result text,
  membership_applied_at timestamptz,
  paid_at timestamptz,
  expires_at timestamptz,
  created_at timestamptz not null default now(),
  updated_at timestamptz not null default now(),
  constraint payment_orders_status_check check (
    status in ('pending', 'confirming', 'paid', 'completed', 'failed', 'expired', 'refunded', 'partially_paid')
  ),
  constraint payment_orders_plan_family_check check (
    plan_family in ('pro', 'max', 'lifetime')
  )
);

create unique index if not exists payment_orders_provider_payment_id_uidx
  on public.payment_orders(provider_payment_id)
  where provider_payment_id is not null;

create unique index if not exists payment_orders_provider_order_id_uidx
  on public.payment_orders(provider_order_id)
  where provider_order_id is not null;

create index if not exists payment_orders_user_created_idx
  on public.payment_orders(user_id, created_at desc);

create table if not exists public.payment_webhook_events (
  id bigint generated by default as identity primary key,
  provider text not null default 'nowpayments',
  event_hash text not null,
  provider_payment_id text,
  signature text,
  signature_valid boolean not null default false,
  payload jsonb not null,
  received_at timestamptz not null default now(),
  processed_at timestamptz,
  process_result text,
  error_message text
);

create unique index if not exists payment_webhook_events_hash_uidx
  on public.payment_webhook_events(event_hash);

create index if not exists payment_webhook_events_payment_id_idx
  on public.payment_webhook_events(provider_payment_id);

create or replace function public.set_payment_updated_at()
returns trigger
language plpgsql
as $$
begin
  new.updated_at := now();
  return new;
end;
$$;

