create or replace function public.create_payment_order(
  p_plan_id bigint,
  p_pay_currency text default 'USDT'
)
returns jsonb
language plpgsql
security definer
as $$
declare
  v_user_id uuid;
  v_plan record;
  v_order public.payment_orders;
  v_pay_currency text;
begin
  v_user_id := auth.uid();
  if v_user_id is null then
    return jsonb_build_object('success', false, 'message', 'NOT_AUTHENTICATED');
  end if;

  select *
  into v_plan
  from public.membership_plans
  where id = p_plan_id and is_active = true;

  if v_plan is null then
    return jsonb_build_object('success', false, 'message', 'PLAN_NOT_AVAILABLE');
  end if;

  v_pay_currency := upper(trim(coalesce(p_pay_currency, 'USDT')));

  if v_pay_currency not in ('USDT', 'BTC', 'ETH', 'SOL', 'BNB', 'XRP', 'DOGE') then
    return jsonb_build_object('success', false, 'message', 'PAY_CURRENCY_NOT_SUPPORTED');
  end if;

  insert into public.payment_orders (
    user_id,
    plan_id,
    plan_name,
    plan_family,
    plan_duration,
    price_amount,
    price_currency,
    pay_currency,
    status
  ) values (
    v_user_id,
    v_plan.id,
    v_plan.name,
    case
      when v_plan.plan_family in ('pro', 'max', 'lifetime') then v_plan.plan_family
      when v_plan.duration::text = 'lifetime' then 'lifetime'
      else 'pro'
    end,
    v_plan.duration::text,
    v_plan.price,
    upper(v_plan.currency),
    v_pay_currency,
    'pending'
  )
  returning * into v_order;

  return jsonb_build_object(
    'success', true,
    'order_id', v_order.id,
    'plan_id', v_order.plan_id,
    'plan_name', v_order.plan_name,
    'plan_family', v_order.plan_family,
    'plan_duration', v_order.plan_duration,
    'price_amount', v_order.price_amount,
    'price_currency', v_order.price_currency,
    'pay_currency', v_order.pay_currency,
    'status', v_order.status
  );
end;
$$;

create or replace function public.apply_membership_from_paid_order(
  p_order_id uuid
)
returns jsonb
language plpgsql
security definer
as $$
declare
  v_order public.payment_orders;
  v_user_row record;
  v_current_tier text;
  v_current_expires timestamptz;
  v_current_level int;
  v_new_level int;
  v_new_tier text;
  v_new_expires timestamptz;
  v_base_time timestamptz;
  v_duration_interval interval;
begin
  select *
  into v_order
  from public.payment_orders
  where id = p_order_id
  for update;

  if v_order is null then
    return jsonb_build_object('success', false, 'message', 'ORDER_NOT_FOUND');
  end if;

  if v_order.membership_applied then
    return jsonb_build_object('success', true, 'message', 'ALREADY_APPLIED');
  end if;

  if v_order.status not in ('paid', 'completed') then
    return jsonb_build_object('success', false, 'message', 'ORDER_NOT_PAID');
  end if;

  select membership_tier::text, membership_expires_at
  into v_current_tier, v_current_expires
  from public.users
  where auth_user_id = v_order.user_id;

  if v_current_tier is null then
    v_current_tier := 'free';
  end if;

  if v_current_tier in ('pro', 'max') and v_current_expires is not null and v_current_expires < now() then
    v_current_tier := 'free';
    v_current_expires := null;
  end if;

  v_current_level := case v_current_tier
    when 'free' then 0
    when 'pro' then 1
    when 'max' then 2
    when 'lifetime' then 3
    else 0
  end;

  v_new_tier := case
    when v_order.plan_family in ('pro', 'max', 'lifetime') then v_order.plan_family
    when v_order.plan_duration = 'lifetime' then 'lifetime'
    else 'pro'
  end;

  v_new_level := case v_new_tier
    when 'pro' then 1
    when 'max' then 2
    when 'lifetime' then 3
    else 0
  end;

  if v_current_level > v_new_level then
    update public.payment_orders
    set membership_applied = true,
        membership_apply_result = 'CANNOT_DOWNGRADE',
        membership_applied_at = now(),
        status = 'completed'
    where id = v_order.id;

    return jsonb_build_object(
      'success', true,
      'applied', false,
      'message', 'CANNOT_DOWNGRADE',
      'current_tier', v_current_tier,
      'target_tier', v_new_tier
    );
  end if;

  if v_new_tier = 'lifetime' then
    v_new_expires := null;
  else
    if v_current_expires is not null and v_current_expires > now() then
      v_base_time := v_current_expires;
    else
      v_base_time := now();
    end if;

    v_duration_interval := case v_order.plan_duration
      when 'monthly' then interval '1 month'
      when 'quarterly' then interval '3 months'
      when 'yearly' then interval '1 year'
      else interval '1 month'
    end;

    v_new_expires := v_base_time + v_duration_interval;
  end if;

  update public.users
  set membership_tier = v_new_tier::public.membership_tier,
      membership_expires_at = v_new_expires
  where auth_user_id = v_order.user_id;

  update public.payment_orders
  set membership_applied = true,
      membership_apply_result = 'APPLIED',
      membership_applied_at = now(),
      paid_at = coalesce(paid_at, now()),
      status = 'completed'
  where id = v_order.id;

  return jsonb_build_object(
    'success', true,
    'applied', true,
    'new_tier', v_new_tier,
    'new_expires_at', v_new_expires
  );
end;
$$;

create or replace function public.get_payment_orders(
  p_limit integer default 20,
  p_offset integer default 0
)
returns jsonb
language plpgsql
stable
security definer
as $$
declare
  v_user_id uuid;
  v_records jsonb;
  v_total bigint;
begin
  v_user_id := auth.uid();
  if v_user_id is null then
    return jsonb_build_object('success', false, 'message', 'NOT_AUTHENTICATED');
  end if;

  select count(*)
  into v_total
  from public.payment_orders
  where user_id = v_user_id;

  select coalesce(jsonb_agg(row_to_json(r)::jsonb order by r.created_at desc), '[]'::jsonb)
  into v_records
  from (
    select
      id,
      plan_name,
      plan_family,
      plan_duration,
      price_amount,
      price_currency,
      pay_amount,
      pay_currency,
      pay_address,
      tx_hash,
      provider,
      provider_order_id,
      provider_payment_id,
      provider_status,
      status,
      invoice_url,
      paid_at,
      created_at
    from public.payment_orders
    where user_id = v_user_id
    order by created_at desc
    limit p_limit
    offset p_offset
  ) r;

  return jsonb_build_object(
    'success', true,
    'total', v_total,
    'records', v_records
  );
end;
$$;

